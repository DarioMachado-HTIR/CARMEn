def game_loop(args):
    os.environ["SDL_JOYSTICK_ALLOW_BACKGROUND_EVENTS"] = "1"
    pygame.init()
    pygame.font.init()
    world = None

    try:      
        client = carla.Client(args.host, args.port)
        client.set_timeout(2.0)

        display = pygame.display.set_mode(
            (args.width, args.height),
            pygame.HWSURFACE | pygame.DOUBLEBUF)

        hud = HUD(args.width, args.height)
        try:
            world = World(client.get_world(), hud, args.filter)
        except:
            print('\nNo World Found! Make sure CARLA is running!')
            exit_program()

        controller = DualControl(world, args.autopilot, args.keycontrol)

        clock = pygame.time.Clock()

        #args.unique_waypoints = generate_unique_waypoints(world.world)

        while True:
            clock.tick_busy_loop(120)
            if controller.parse_events(world, clock, args.keycontrol):
                return
            world.tick(clock, args)
            world.render(display)
            pygame.display.flip()

    finally:
        print('\nClosed by User. Bye!')
        if args.rec:
            print('\nRecording Stoppped. Closing file.')
            # close the file
            args.f.close()
        if world is not None:
            world.destroy()

        pygame.quit()


